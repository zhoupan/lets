package lets.core.app.model;

/** OptionItem. */
public class OptionItem implements java.io.Serializable {

  /** The name. */
  private String name;

  /** The value. */
  private String value;

  /**
   * With name.
   *
   * @param name the value
   * @return the option item
   */
  public OptionItem withName(String name) {
    this.setName(name);
    return this;
  }

  /**
   * With value.
   *
   * @param value the value
   * @return the option item
   */
  public OptionItem withValue(String value) {
    this.setValue(value);
    return this;
  }

  /**
   * Gets the name.
   *
   * @return the name
   */
  public String getName() {
    return name;
  }

  /**
   * Sets the name.
   *
   * @param name the new name
   */
  public void setName(String name) {
    this.name = name;
  }

  /**
   * Gets the value.
   *
   * @return the value
   */
  public String getValue() {
    return value;
  }

  /**
   * Sets the value.
   *
   * @param value the new value
   */
  public void setValue(String value) {
    this.value = value;
  }

  /**
   * Hash code.
   *
   * @return the int
   */
  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = (prime * result) + ((name == null) ? 0 : name.hashCode());
    return result;
  }

  /**
   * Equals.
   *
   * @param obj the obj
   * @return true, if successful
   */
  @Override
  public boolean equals(Object obj) {
    if (this == obj) {
      return true;
    }
    if (obj == null) {
      return false;
    }
    if (this.getClass() != obj.getClass()) {
      return false;
    }
    OptionItem other = (OptionItem) obj;
    if (name == null) {
      if (other.name != null) {
        return false;
      }
    } else if (!name.equals(other.name)) {
      return false;
    }
    return true;
  }

  /**
   * To string.
   *
   * @return the string
   */
  @Override
  public String toString() {
    return "OptionItem [name=" + name + ", value=" + value + "]";
  }
}
